    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
                    try {
            PreparedStatement pps = cc.prepareStatement("INSERT INTO ingreso(IdIngreso, Marca, Modelo, Color,"
                    + "Placas, Nivel, Cajon, FecHoraIngreso, IdAcomodador) VALUES(?,?,?,?,?,?,?,?,?)");
            
            pps.setString(1, txtFolio.getText());
            pps.setString(2, txtMarca.getText());
            pps.setString(3, txtModelo.getText());
            pps.setString(4, txtColor.getText());
            pps.setString(5, txtPlacas.getText());
            pps.setString(6, txtNivel.getText());
            pps.setString(7, txtCajon.getText());
            pps.setString(8, txtFechaIngreso.getText());
            pps.setString(9, txtAcomodador.getText());
            pps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Datos Ingresados Correctamente");
            //mostrarTabla();

        } catch (Exception e) {
            Logger.getLogger(Altas.class.getName()).log(Level.SEVERE, null, e);
            JOptionPane.showMessageDialog(null, "Datos No Ingresados");
                   
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed
