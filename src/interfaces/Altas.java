    // Para la creación de las tablas en consulta
    void mostrarTabla(){
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("FOLIO");
        modelo.addColumn("MARCA");
        modelo.addColumn("MODELO");
        modelo.addColumn("COLOR");
        modelo.addColumn("PLACAS");
        modelo.addColumn("NIVEL");
        modelo.addColumn("CAJON");
        modelo.addColumn("FECHA/HORA INGRESO");
        modelo.addColumn("ID ACOMODADOR");
        //Inserta los datos a la tabla
        tabladatos.setModel(modelo);
        String []datos = new String[9];
        try {
            Statement st = cc.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM ingreso");
            while(rs.next()){
                datos[0]=rs.getString(1);
                datos[1]=rs.getString(2);
                datos[2]=rs.getString(3);
                datos[3]=rs.getString(4);
                datos[4]=rs.getString(5);
                datos[5]=rs.getString(6);
                datos[6]=rs.getString(7);
                datos[7]=rs.getString(8);
                datos[8]=rs.getString(9);
                modelo.addRow(datos);
            }
            tabladatos.setModel(modelo);
        } catch (SQLException ex) {
            Logger.getLogger(Altas.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    

