    //Boton de salida con el metodo y conexión a nuestra base de datos
    private void btnSalidaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalidaActionPerformed
        try {
            PreparedStatement pts = cc.prepareStatement("INSERT INTO salida(IdSalida, FecHoraSalida, FecHoraEntrada"
                    + "Costo) VALUES(?,?,?,?)");
            
            pts.setString(1, txtFolio.getText());
            pts.setString(2, txtFechaSalida.getText());
            pts.setString(3, txtFechaEntrada.getText());
            pts.setString(4, txtCosto.getText());
            pts.executeUpdate();
            JOptionPane.showMessageDialog(null, "Datos Ingresados Correctamente");
            //mostrarTabla();

        } catch (Exception e) {
            Logger.getLogger(Altas.class.getName()).log(Level.SEVERE, null, e);
            JOptionPane.showMessageDialog(null, "Datos No Ingresados");
                   
        }
     
    }//GEN-LAST:event_btnSalidaActionPerformed

